# len() returns the length of set
a={1,2,3,(8,9,7),"Hello"}
print("Length: ",len(a))

# max() returns max integer value from set 
a={1,2,3,4,5,6}
print("Max: ",max(a))

# min() returns min integer value from set 
print("Min: ",min(a))

# sorted() sort set
b={5,6,7,4,9,1,2}
print("Sorted: ", sorted(b))

# sum() returns the sum of only if there is integer values
c={4,2,3,7,6,1}
print("Sum: ",sum(c))