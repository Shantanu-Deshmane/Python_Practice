# Using escape sequwnce Character : 
#  --> \n
print("Hello Everyone! \n I am Shantanu Deshmane")

# --> \t
print("This line is written with \t extra tab space")

# --> \/
print("We can write \\n in string using escape sequence character")

# --> \'
print('I am \'Shantanu\'')

# -->\"
print('I am \"Shantanu\"')

# \b for backspace
print("Here is backspace: Shant\banu")


